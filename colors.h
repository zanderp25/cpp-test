/* Some ANSI codes for adding color to the terminal */

#define RESET "\033[0m"
#define BOLD "\033[2m"
#define BLACK "\033[31m"
#define RED "\033[31m"
#define GREEN "\033[32m"
#define YELLOW "\033[33m"
#define BLUE "\033[34m"
#define MAGENTA "\033[35m"
#define CYAN "\033[36m"
#define LIGHTGRAY "\033[37m"
#define DARKGRAY "\033[90m"